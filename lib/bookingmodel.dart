class Booking {
  final String packageName;
  final String price;
  final String travelerName;
  final String email;
  final String phone;
  final bool addInsurance;

  Booking({
    required this.packageName,
    required this.price,
    required this.travelerName,
    required this.email,
    required this.phone,
    required this.addInsurance,
  });
}
